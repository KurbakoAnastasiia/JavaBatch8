package com.syntax.class02;

public class ArithemeticOperations {

	public static void main(String[] args) {
		
		int num1, num2;
		 num1=10;
		 num2=12;
		 
		 System.out.println(num1+num2);
		 
		 System.out.println(num1-num2);
		
		double num3=10.99;
		double num4=2.99;
		
		double milt=num3*num4;
		
		System.out.println(milt);
		
		double div=num3/num4;
		
		System.out.println(div);
		
		float f=10.99f;
		float f1=2.99f;
		
		System.out.println("The result of division of 2 doubles values = "+f/f1);

	}

}
