package com.syntax.class02;

public class ArithmeticOperatorsTask {

	public static void main(String[] args) {
		
		double num1=2.75;
		double num2=0.29;
		
		double add=num1+num2;
		double subtract=num1-num2;
		double mult=num1*num2;
		double div=num1/num2;
		
		
		System.out.println("The addition of 2 numbers "+num1+" and "+num2+" is equal to "+add);
		System.out.println("The subtraction of 2 numbers "+num1+" and "+num2+" is equal to "+subtract);
		System.out.println("The multilpication of 2 numbers "+num1+" and "+num2+" is equal to "+mult);
		System.out.println("The division of 2 numbers "+num1+" and "+num2+" is equal to "+div);
		
		
		double num3=3.9;
		
		double square=num3*num3;
	
		
		
		System.out.println("The square of the "+num3+" is "+square);
		
		
		int widht=5;
		int height=8;
		
		int area=widht*height;
		int per=2*(widht+height);
		
		System.out.println("The perimeter of a rectangle with widht "+widht+" and height "+height+" is equal to "+per+" and the area is "+area);
		

		
	}

}
