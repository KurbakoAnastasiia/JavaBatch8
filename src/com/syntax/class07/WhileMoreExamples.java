public class WhileMoreExamples {

	public static void main(String[] args) {
		
		String day = "Saturday";
		
		while (day.equals("Saturday")) {
		System.out.println("My favourite Java Class");
		day = "Sunday";
		
		}
		boolean isItBreak = true;
		
		while (isItBreak) {
			System.out.println("I will get a tea");
			isItBreak = false;
		}
		}

	}


